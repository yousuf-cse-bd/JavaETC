package JavaETC;
//Java takes faction value as double data-typed

public class DefaultDouble {
    public static void main(String[] args) {
        double myDouble = Math.acos(-1); //PI value
        System.out.println(myDouble);

        float myFloat = (float) Math.acos(-1);
        System.out.println(myFloat);

        float tempFloat = 3.1425927F;
        System.out.println(tempFloat);
    }
}
